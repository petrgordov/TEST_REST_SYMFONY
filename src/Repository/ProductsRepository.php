    public function findInfoByProduct(int $id): array
    {
        $conn = $this->getEntityManager()->getConnection();

        $sql = '
            SELECT p.*,pr.price,d.val deposit FROM products p
                LEFT JOIN prices pr ON pr.id_product=p.id 
                LEFT JOIN deposits d ON d.id_product=p.id 
            WHERE p.id = :id 
            ';
        $stmt = $conn->prepare($sql);
        $resultSet = $stmt->executeQuery(['id' => $id]);

        // returns an array of arrays (i.e. a raw data set)
        return $resultSet->fetchAllAssociative();
    }
